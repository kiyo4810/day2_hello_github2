// 1. myName という名前の箱（定数）を作り、"キヨヒコ" という文字を入れます
const myName = "キヨヒコ";

// 2. generateGreeting という「挨拶を作る手順（関数）」を定義します
// name というのは、後で外から受け取る「名前の入れ物」です
const generateGreeting = (name) => {
    
    // 3. もし（if）、受け取った name が "キヨヒコ" と完全に一致したら…
    if (name === "キヨヒコ") {
        // 4. キヨヒコさん専用の、この特別な文章を外（実行結果）に返します
        return `あ！キヨヒコさん、今日もお疲れ様です！100日チャレンジ、順調ですね。`;
    } 
    // 5. そうでなければ（else）、つまりキヨヒコ以外の名前だったら…
    else {
        // 6. 誰にでも使える、この一般的な文章を外に返します
        return `こんにちは、${name}さん！はじめまして。`;
    }
}; // 7. ここで「挨拶を作る手順」の定義はおわりです

// 8. さきほどの手順を実行し、結果を message という新しい箱に保存します
// ここで myName（"キヨヒコ"）を関数の中に投げ込んでいます
const message = generateGreeting(myName);

// 9. message という箱の中身を、コンソール（黒い画面）に表示します
console.log(message);

/* ============================================================
【全体像のまとめ】
1. 材料（変数）：myName
   - プログラムで使うデータを準備する場所。
2. レシピ（関数）：generateGreeting
   - 「もし〜なら（if）」という判断基準を使い、データの加工手順を決める。
3. 調理（実行）：message = generateGreeting(myName)
   - 実際に材料をレシピに渡して、完成品（メッセージ）を作る。
4. 配膳（出力）：console.log
   - 出来上がったものをユーザーが見える場所に提示する。
============================================================
*/